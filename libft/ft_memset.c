#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*buf;

	buf = (char*)s;
	i = 0;
	while (i < n)
	{
		buf[i] = (char)c;
		i++;
	}
	return (s);
}
