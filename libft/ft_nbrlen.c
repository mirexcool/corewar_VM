#include "libft.h"

int		ft_nbrlen(int i)
{
	int	s;
	int c;

	s = 0;
	c = 0;
	if (-2147483648 == i)
		return (11);
	if (i == 0)
		return (1);
	if (i < 0)
	{
		s = 1;
		i = i * -1;
	}
	while (i > 0)
	{
		i = i / 10;
		c++;
	}
	return (c + s);
}
